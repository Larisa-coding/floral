#!/bin/bash

# Цвета для вывода
GREEN='\033[0;32m'
RED='\033[0;31m'
YELLOW='\033[0;33m'
NC='\033[0m' # No Color

echo -e "${YELLOW}======================================================${NC}"
echo -e "${YELLOW}      Скрипт обновления базы данных Floral&Petal      ${NC}"
echo -e "${YELLOW}======================================================${NC}"
echo ""

# Запуск миграций Django
echo -e "${YELLOW}[1/3]${NC} Запуск миграций Django..."
python manage.py makemigrations
python manage.py migrate
if [ $? -eq 0 ]; then
    echo -e "${GREEN}✓ Миграции успешно выполнены${NC}"
else
    echo -e "${RED}✗ Возникла ошибка при выполнении миграций${NC}"
    echo -e "${YELLOW}Запуск исправления базы данных...${NC}"
fi

# Запуск скрипта для исправления структуры базы данных
echo -e "${YELLOW}[2/3]${NC} Запуск скрипта исправления структуры базы данных..."
python fix_database.py
if [ $? -eq 0 ]; then
    echo -e "${GREEN}✓ Скрипт исправления успешно выполнен${NC}"
else
    echo -e "${RED}✗ Возникла ошибка при исправлении базы данных${NC}"
fi

# Проверка целостности базы данных
echo -e "${YELLOW}[3/3]${NC} Проверка целостности базы данных..."
python manage.py check
if [ $? -eq 0 ]; then
    echo -e "${GREEN}✓ Проверка целостности успешно выполнена${NC}"
else
    echo -e "${RED}✗ При проверке целостности базы данных обнаружены ошибки${NC}"
fi

echo ""
echo -e "${YELLOW}======================================================${NC}"
echo -e "${GREEN}      Обновление базы данных завершено!      ${NC}"
echo -e "${YELLOW}======================================================${NC}"
echo ""
echo -e "Для запуска сервера выполните команду:"
echo -e "${GREEN}python manage.py runserver 0.0.0.0:8000${NC}"
echo ""

